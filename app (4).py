import streamlit as st
import joblib
import numpy as np

# --- Page Setup ---
st.set_page_config(page_title="Diabetes Prediction App", layout="centered")
st.title("ü©∫ AI Diabetes Prediction App")
st.write("This app predicts the likelihood of a person having diabetes based on medical inputs. *(Educational Purpose Only)*")

# --- Load model and scaler ---
model = joblib.load('diabetes_model.pkl')
scaler = joblib.load('scaler.pkl')

# --- Input fields ---
st.header("Enter Patient Details:")
col1, col2 = st.columns(2)

with col1:
    pregnancies = st.number_input("Pregnancies", min_value=0, max_value=20, value=2)
    glucose = st.number_input("Glucose Level", min_value=0, max_value=300, value=120)
    blood_pressure = st.number_input("Blood Pressure", min_value=0, max_value=200, value=70)
    skin_thickness = st.number_input("Skin Thickness", min_value=0, max_value=100, value=20)

with col2:
    insulin = st.number_input("Insulin Level", min_value=0, max_value=900, value=85)
    bmi = st.number_input("BMI", min_value=0.0, max_value=70.0, value=25.0)
    dpf = st.number_input("Diabetes Pedigree Function", min_value=0.0, max_value=3.0, value=0.5)
    age = st.number_input("Age", min_value=1, max_value=120, value=33)

# --- Predict button ---
if st.button("üîç Predict Diabetes Risk"):
    input_data = np.array([[pregnancies, glucose, blood_pressure, skin_thickness, insulin, bmi, dpf, age]])
    input_scaled = scaler.transform(input_data)
    prediction = model.predict(input_scaled)
    probability = model.predict_proba(input_scaled)[0][1]

    st.subheader("üîî Prediction Result:")
    if prediction[0] == 1:
        st.error(f"‚ö†Ô∏è High Risk of Diabetes! (Probability: {probability*100:.2f}%)")
    else:
        st.success(f"‚úÖ Low Risk of Diabetes (Probability: {probability*100:.2f}%)")

    st.caption("Disclaimer: This result is generated by a machine learning model and is not a medical diagnosis.")
